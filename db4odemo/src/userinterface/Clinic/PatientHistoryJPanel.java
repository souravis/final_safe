    public void populateAssessmentHistory() {
        DefaultTableModel model = (DefaultTableModel) tblAssessmentHistory.getModel();

        model.setRowCount(0);
        for (Assessment assessment : patient.getAssessments()) {
            Object[] row = new Object[6];
            row[0] = DateUtils.dateToString(assessment.getCreatedDate());
            row[1] = assessment;
            UserAccount doctor = assessment.getDoctor();
            if (doctor != null) {
                row[2] = doctor.getFirstName();
            } else {
                row[2] = "N/A";
            }
            UserAccount counsellor = assessment.getCounsellor();
            if (counsellor != null) {
                row[3] = counsellor.getFirstName();
            } else {
                row[3] = "N/A";
            }
            row[4] = assessment.getScore();
            row[5] = assessment.getZone();

            model.addRow(row);
        }
    }
