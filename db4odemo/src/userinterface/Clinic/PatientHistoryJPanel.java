    private void viewButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewButtonActionPerformed
        // TODO add your handling code here:
        int selectedrow = tblAssessmentHistory.getSelectedRow();

        if (selectedrow >= 0) {
            Assessment request = (Assessment)tblAssessmentHistory.getValueAt(selectedrow, 1);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            PatientAssessmentDetailJPanel counselorAssessmentJPanel = new PatientAssessmentDetailJPanel(userProcessContainer, userAccount, enterprise, patient, request);
            userProcessContainer.add("CounselorPatientHistoryJPanel", counselorAssessmentJPanel);
            layout.next(userProcessContainer);

        } else {
            JOptionPane.showMessageDialog(null, " Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_viewButtonActionPerformed

