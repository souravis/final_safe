    private void btnMakeInActiveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMakeInActiveActionPerformed

        int row = tblPersons.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Pls select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int personId = (int) tblPersons.getValueAt(row, 0);
        ecoSystem.makeInActive(personId);
        populatePersonTable();

    }//GEN-LAST:event_btnMakeInActiveActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed

        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length - 1];

        if (component instanceof ManageEmployeeJPanel) {
            ManageEmployeeJPanel manageEmployeeJPanel = (ManageEmployeeJPanel) component;
            manageEmployeeJPanel.populatePersonCombo();
        }
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        int row = tblPersons.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Pls select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int personId = (int) tblPersons.getValueAt(row, 0);
        Person p = ecoSystem.findById(personId);
        if (!p.isActive()) {
            JOptionPane.showMessageDialog(null, "Person is InActive, Please select one that is Active!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        UpdatePersonJPanel updatePersonJPanel = new UpdatePersonJPanel(userProcessContainer, p);
        userProcessContainer.add("UpdatePersonJPanel", updatePersonJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);

    }//GEN-LAST:event_btnUpdateActionPerformed

