    public void populatePersonTable() {

        DefaultTableModel model = (DefaultTableModel) tblPersons.getModel();
        model.setRowCount(0);

        for (Person person : ecoSystem.getAllPersons()) {

            Object[] row = new Object[4];
            row[0] = person.getId();
            row[1] = person.getFirstName();
            row[2] = person.getLastName();
            String status = "Active";
            if (!person.isActive()) {
                status = "In Active";
            }
            row[3] = status;
            model.addRow(row);

        }

    }

