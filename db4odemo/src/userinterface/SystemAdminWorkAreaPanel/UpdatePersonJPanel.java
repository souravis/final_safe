    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String firstName = txtFirstName.getText();
        String lastName = txtLastName.getText();
        String street = txtStreet.getText();
        String city = txtCity.getText();
        String state = txtState.getText();
        String zip = txtZip.getText();
        String phone = txtPhone.getText();
        String email = txtEmail.getText();

        if (RegexUtils.isEmpty(firstName, "Please enter First Name")) {
            return;
        }

        if (RegexUtils.isEmpty(lastName, "Please enter Last Name")) {
            return;
        }

        if (RegexUtils.isEmpty(street, "Please enter Street Name")) {
            return;
        }
        if (RegexUtils.isEmpty(city, "Please enter value for city ")) {
            return;
        }
        if (RegexUtils.isEmpty(state, "Please enter value for state")) {
            return;
        }
        if (RegexUtils.isEmpty(zip, "Please enter value for Zip Code")) {
            return;
        }

        if (!RegexUtils.isInteger(zip, "Please enter valid Zip Code")) {
            return;
        }

        if (RegexUtils.isEmpty(phone, "Please enter Phone Number")) {
            return;
        }

        if (!RegexUtils.isInteger(phone, "Please enter valid Phone Number")) {
            return;
        }

        if (RegexUtils.isEmpty(email, "Please enter email Address")) {
            return;
        }

        person.setFirstName(firstName);
        person.setLastName(lastName);
        person.setStreet(street);
        person.setCity(city);
        person.setState(state);
        person.setZipcode(zip);
        person.setContactNo(phone);
        person.setEmailAddress(email);

        JOptionPane.showMessageDialog(null, "Person Updated Successfully");
    }//GEN-LAST:event_btnUpdateActionPerformed

